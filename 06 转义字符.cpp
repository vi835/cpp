# include <iostream>
using namespace std;

int main() {
	// 转义字符
	cout << " hello wordl" << endl;

	cout << "hello c ++\n";

	// 反斜杠 //
	cout << "\\" << endl;

	// u水平制表符 \t 作用可以整齐输出数据
	cout << "aaaaa\thelloworld" << endl;
	cout << "aa\thelloworld" << endl;
	cout << "aaaa\thelloworld" << endl;
	system("pause");
	return 0;
}