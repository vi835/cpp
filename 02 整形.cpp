# include <iostream>
using namespace std;

int main002() {
	// 整形
	// 1 短整型(-32768 ~ 32767)
	short num1 = 32768;
	// 2 整形(最常用)
	int num2 = 10;
	// 3 长整型
	long num3 = 10;
	// 3 长长整形
	long long num4 = 10;

	cout << "num1 = " << num1 << endl;
	cout << "num2 = " << num2 << endl;
	cout << "num3 = " << num3 << endl;
	cout << "num4 = " << num4 << endl;
	return 0;
}